package com.example.tangclan;

public abstract class Shape {
    int x;
    int y;

    // Setters and Getters
    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public class Shape {
        String color = "purple";
    }


}
